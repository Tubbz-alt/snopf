characters = 'abcdefghijklmnopqrABCDEFGHIJKLMNOPQR0123456789-@!?$*+=&#[]().:><'

def to_bytes(character_table):
    b = []
    for c in characters:
        kc = character_table[c]
        b.append(int.to_bytes(kc[0], 1, 'little'))
        b.append(int.to_bytes(kc[1], 1, 'little'))
    return b''.join(b)
